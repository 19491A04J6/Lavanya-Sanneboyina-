package com.learn.TypeCasting;

public class ExplicitTypeCasting {

	public static void main(String[] args) {
		int a =10;
		long b=2000;
		
		short c=(short)a;
		byte d=(byte)b;
		System.out.println(c);
		System.out.println(d);
		
		
		double dl=3.98765423;
		float f=(float)dl;
		System.out.println(f);

	}

}
